const I18n = {
  currentLang: 'en',
  
  init() {
    this.currentLang = localStorage.getItem('rc_lang') || 'en';
    document.documentElement.lang = this.currentLang;
  },
  
  setLanguage(lang) {
    if (TRANSLATIONS[lang]) {
      this.currentLang = lang;
      localStorage.setItem('rc_lang', lang);
      document.documentElement.lang = lang;
      
      // Trigger a re-render
      if (typeof App !== 'undefined' && App.render) {
        App.render();
      }
    }
  },
  
  t(key) {
    const strings = TRANSLATIONS[this.currentLang] || TRANSLATIONS['en'];
    return strings[key] || TRANSLATIONS['en'][key] || key;
  }
};

// Auto-initialize on load
I18n.init();
